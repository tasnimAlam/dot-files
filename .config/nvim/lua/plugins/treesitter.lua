				["[m"] = "@function.outer",
				["[s"] = { query = "@local.scope", query_group = "locals", desc = "Previous scope" },
			},
			goto_previous_end = {
				["[M"] = "@function.outer",
			},
		},
	},
	context_commentstring = {
		enable = true,
		config = {
